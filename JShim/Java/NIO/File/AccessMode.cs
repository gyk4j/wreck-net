
using System;

namespace Java.NIO.File
{
	/// <summary>
	/// Defines access modes used to test the accessibility of a file.
	/// </summary>
	public enum AccessMode
	{
		/// <summary>
		/// Test execute access.
		/// </summary>
		Execute,
		
		/// <summary>
		/// Test read access.
		/// </summary>
		Read,
		
		/// <summary>
		/// Test write access.
		/// </summary>
		Write
	}
}
